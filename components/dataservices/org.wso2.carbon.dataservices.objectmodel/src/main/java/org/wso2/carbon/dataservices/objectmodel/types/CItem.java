package org.wso2.carbon.dataservices.objectmodel.types;

/**
 * This class represents a base item of the object model.
 */
public abstract class CItem {

	private String name;
	
	private String source;
	
	public CItem(String name, String source) {
		this.name = name;
		this.source = source;
	}
	
	public CItem(String name) {
		this(name, null);
	}
	
	public CItem() {
		this(null, null);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}
	
}
